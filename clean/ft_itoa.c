#include "libft.h"

char	*ft_itoa(int n)
{
	int		i;
	int		nb;
	char	*str;

	nb = (n < 0) ? -n : n;
	i = (n < 0) ? 1 : 0;
	while ((nb /= 10))
		i++;
	if ((str = malloc(sizeof(char) * (8))) == 0)
		return (0);
	if(n < 0)
	{
		*str++ = '-';
		i--;
	}
	str += i;
	while (i)
	{
		*str-- = n % 10 + '0';
		n /= 10;
		i--;
	}
	return (str);
}
