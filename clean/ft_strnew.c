#include "libft.h"

char	*ft_strnew(size_t i)
{
	char *str;
	char *s;

	if ((str = malloc(sizeof(char) * (i + 1))) == 0)
		return (0);
	s = str;
	while (i--)
	{
		*s++ = 0;
	}
	*s = 0;
	return (str);
}
